    private void jSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSimpanActionPerformed
        // TODO add your handling code here:
        sumTotal = 0;
        int row = jTable1.getRowCount();
        
        try {
            ResultSet rs=db.ambildata("select * from trans_jual where no_transjual ='"+jNoTrans.getText()+"'");
            if (rs.next()){
                JOptionPane.showMessageDialog(null, "No transaksi sudah terdaftar");
            }else{
                for (int i = 0; i < row; i++){
                    db.aksi("INSERT INTO tbdetail_jual(no_trans,tanggal,id_barang,jml_barang,hrg_barang)values" 
                     + "('" + jNoTrans.getText() + "','" + jTanggal.getText() + "','" + jTable1.getValueAt(i, 0) + "',"
                             + "'" + jTable1.getValueAt(i, 3) + "','" + jTable1.getValueAt(i, 2) + "')");
                    db.aksi("update tb_barang set jml_barang=jml_barang-" + jTable1.getValueAt(i, 3) + " where id_barang='" +
                            jTable1.getValueAt(i, 0) + "'");
                }
                db.aksi("insert into trans_jual (no_transjual,tanggal,total_harga)values('" + jNoTrans.getText() + "'," 
                        + "'" + jNoTrans.getText() + "','" + jtotal.getText() + "')");
                JOptionPane.showMessageDialog(null, "Data telah tersimpan");
                
                //}
                model.setRowCount(0);
                buatnomor();
                // } catch (Exception e) {
                // JOptionPane.showMessageDialog(null, e);
                
            }} catch (SQLException ex) {
                Logger.getLogger(FrmTransjual.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jSimpanActionPerformed

