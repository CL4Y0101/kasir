    public void buatnomor(){
        try {
            ResultSet rs = db.ambildata("Select no_trans as auto from tbtrans_jual ORDER by no_trans desc");
            
            if (rs.next()){
                int no_urut = Integer.parseInt(rs.getString("auto")) + 1;
                jNoTrans.setText(Integer.toString(no_urut));
            } else {
                int not_t = 1;
                jNoTrans.setText(Integer.toString(not_t));
            }
            rs.close();
        } catch (Exception e) {
        }
    }
    
