    private void jKodeBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jKodeBarangActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rs = db.ambildata("select * from tb_barang where id_barang='" +jKodeBarang.getText()+"'");
             if (rs.next()) {
                 jNamaBarang.setText(rs.getString("nm_barang"));
                 jHarga.setText(rs.getString("hrg_barang"));
                 jStok.setText(rs.getString("jml_barang"));
                 jBeli.requestFocus();
             }else{
                 JOptionPane.showMessageDialog(null, "Kode Belum terdaftar");
                 jKodeBarang.selectAll();
             }       
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jKodeBarangActionPerformed

    private void jBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBeliActionPerformed
        // TODO add your handling code here:
        int a1, a2, total, stok_barang = Integer.parseInt(jStok.getText());
        if (stok_barang > 1) {
            a1 = Integer.parseInt(jHarga.getText());
            a2 = Integer.parseInt(jBeli.getText());
            total = a1 * a2;
            jJumlahHarga.setText(String.valueOf(total));
            jTambah.requestFocus();
    }else{
            JOptionPane.showMessageDialog(null, "Maaf, \nstok sudah mencapai stok minimum!");   
        }
    }//GEN-LAST:event_jBeliActionPerformed

    private void jTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTambahActionPerformed
        // TODO add your handling code here:
        try {
            //konfirmasi tambah
         int n = JOptionPane.showConfirmDialog(this, "Tambah lagi?", "Dia Risky Ananda",
                 JOptionPane.YES_NO_OPTION,
                 JOptionPane.QUESTION_MESSAGE, null);
         if (n == JOptionPane.YES_OPTION){
             long qyt, hrg;
             
             model.addRow(new Object[]{jKodeBarang.getText(), jNamaBarang.getText(), jHarga.getText(), jBeli.getText()});
             jTable1.setModel(model);
             qyt = Long.parseLong(jBeli.getText());
             hrg = Long.parseLong(jHarga.getText());
             sumTotal += (hrg * qyt);
             jJumlahHarga.setText(String.valueOf(sumTotal));
             jKodeBarang.requestFocus();
             jKodeBarang.setText("");
             jNamaBarang.setText("");
             jHarga.setText("");
             jBeli.setText("");
             jJumlahHarga.setText("");
         }else if (n == JOptionPane.NO_OPTION){
             long qyt, hrg;
             model.addRow(new Object[]{jKodeBarang.getText(), jNamaBarang.getText(), jHarga.getText(), jBeli.getText()});
             jTable1.setModel(model);
             qyt = Long.parseLong(jBeli.getText());
             hrg = Long.parseLong(jHarga.getText());
             sumTotal += (hrg * qyt);
             jJumlahHarga.setText(String.valueOf(sumTotal));
             jKodeBarang.requestFocus();
             jKodeBarang.setText("");
             jNamaBarang.setText("");
             jHarga.setText("");
             jBeli.setText("");
             jJumlahHarga.setText("");
             jbayar.requestFocus();
             
         }else{
             jbayar.requestFocus();
         }
        } catch (Exception e) {
        }
    
    }//GEN-LAST:event_jTambahActionPerformed

